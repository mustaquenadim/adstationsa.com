// Function to generate Arabic-specific metadata
export function generateArabicSEO(
  pageKey: keyof typeof arabicSEOContent,
  url: string,
  locale: string = 'ar'
): Metadata {
  const content = arabicSEOContent[pageKey];
  
  return generateSEO({
    title: content.title,
    description: content.description,
    keywords: content.keywords,
    url,
    locale,
    type: 'website'
  });
}

// Helper function to get localized metadata
export function getLocalizedMetadata(
  englishTitle: string,
  englishDescription: string,
  englishKeywords: string[],
  arabicPageKey: keyof typeof arabicSEOContent,
  url: string,
  locale: string = 'en'
): Metadata {
  if (locale === 'ar') {
    return generateArabicSEO(arabicPageKey, url, locale);
  }
  
  return generateSEO({
    title: englishTitle,
    description: englishDescription,
    keywords: englishKeywords,
    url,
    locale,
    type: 'website'
  });
}
